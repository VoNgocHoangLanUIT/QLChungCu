    private void populateStaffTable() {
        tableModel.setRowCount(0);
        List<Staff> staffList = staffService.getAllStaff();
        
        for (Staff staff : staffList) {
            tableModel.addRow(new Object[]{
                staff.getStaffId(),
                staff.getFullName(),
                staff.getGender(),
                staff.getDepartment()
            });
        }
    }

