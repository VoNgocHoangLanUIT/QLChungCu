    private void bookFacilitiesActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bookFacilitiesActionPerformed
        // TODO add your handling code here:
        int selectedRow = staffTable.getSelectedRow();
        if (selectedRow == -1) {
            JOptionPane.showMessageDialog(this, "Please select a staff member to assign.", "No Selection", JOptionPane.WARNING_MESSAGE);
            return;
        }

        String staffId = tableModel.getValueAt(selectedRow, 0).toString();
        String complaintId = complaint.getComplaintId();

        int confirm = JOptionPane.showConfirmDialog(this, 
                "Are you sure you want to assign this complaint to staff ID: " + staffId + "?",
                "Confirm Assignment", 
                JOptionPane.YES_NO_OPTION);

        if (confirm == JOptionPane.YES_OPTION) {
            boolean success = assignmentService.assignComplaintToStaff(complaintId, staffId);
            if (success) {
                JOptionPane.showMessageDialog(this, "Complaint assigned successfully! The status has been updated to 'In Progress'.", "Success", JOptionPane.INFORMATION_MESSAGE);
                this.dispose();
            } else {
                JOptionPane.showMessageDialog(this, "Failed to assign the complaint.", "Database Error", JOptionPane.ERROR_MESSAGE);
            }
        }
    }//GEN-LAST:event_bookFacilitiesActionPerformed

