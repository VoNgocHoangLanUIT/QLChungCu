    private void addButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_addButtonActionPerformed
        // TODO add your handling code here:
        String complaintId = complaintIDField.getText().trim();
        String title = complaintTitleField.getText().trim();
        String apartmentId = complaintByField.getText().trim();
        String status = (String) statusComboBox.getSelectedItem();
        String description = descriptionField.getText().trim();

        if (complaintId.isEmpty() || title.isEmpty() || apartmentId.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Mã phản ánh, Tiêu đề, và Mã căn hộ không được để trống!", "Lỗi", JOptionPane.ERROR_MESSAGE);
            return;
        }

        Complaint newComplaint = new Complaint();
        newComplaint.setComplaintId(complaintId);
        newComplaint.setTitle(title);
        newComplaint.setApartmentId(apartmentId);
        newComplaint.setStatus(status);
        newComplaint.setDescription(description);

        boolean success = complaintService.addComplaint(newComplaint);

        if (success) {
            // Nếu thêm vào DB thành công, và có ảnh được chọn, thì lưu ảnh
            if (selectedImageFile != null) {
                saveComplaintImage(complaintId);
            }
            JOptionPane.showMessageDialog(this, "Thêm phản ánh thành công!");
            this.dispose();
        } else {
            JOptionPane.showMessageDialog(this, "Thêm phản ánh thất bại! Vui lòng kiểm tra lại dữ liệu.", "Lỗi", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_addButtonActionPerformed

