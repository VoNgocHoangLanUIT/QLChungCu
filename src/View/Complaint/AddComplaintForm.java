    private void saveComplaintImage(String complaintId) {
        try {
            // Lấy phần mở rộng của tệp gốc (ví dụ: .jpg, .png)
            String originalFileName = selectedImageFile.getName();
            String extension = "";
            int i = originalFileName.lastIndexOf('.');
            if (i > 0) {
                extension = originalFileName.substring(i);
            }

            // Xác định đường dẫn đích.
            // **LƯU Ý:** Cách này hoạt động tốt trong môi trường phát triển (IDE)
            // nhưng có thể không hoạt động khi đóng gói thành file JAR.
            // Nên sử dụng một thư mục bên ngoài dự án để lưu trữ nội dung do người dùng tải lên.
            Path destinationPath = Paths.get("src/icon/" + complaintId + extension);
            
            // Tạo thư mục cha nếu nó chưa tồn tại
            Files.createDirectories(destinationPath.getParent());

            // Sao chép tệp, ghi đè nếu đã tồn tại
            Files.copy(selectedImageFile.toPath(), destinationPath, StandardCopyOption.REPLACE_EXISTING);

        } catch (IOException e) {
            JOptionPane.showMessageDialog(this, "Lỗi khi lưu hình ảnh: " + e.getMessage(), "Lỗi Lưu Ảnh", JOptionPane.ERROR_MESSAGE);
            e.printStackTrace();
        }
    }

