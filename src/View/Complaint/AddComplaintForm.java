    private void addImageActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_addImageActionPerformed
        // TODO add your handling code here:
        JFileChooser fileChooser = new JFileChooser();
        // Lọc để chỉ hiển thị các tệp hình ảnh
        FileNameExtensionFilter filter = new FileNameExtensionFilter("Image Files", "jpg", "png", "gif", "jpeg");
        fileChooser.setFileFilter(filter);

        int result = fileChooser.showOpenDialog(this);
        if (result == JFileChooser.APPROVE_OPTION) {
            selectedImageFile = fileChooser.getSelectedFile();
            try {
                // Hiển thị hình ảnh đã chọn lên JLabel
                ImageIcon imageIcon = new ImageIcon(selectedImageFile.getAbsolutePath());
                Image image = imageIcon.getImage();
                
                // Thay đổi kích thước ảnh để vừa với JLabel
                Image scaledImage = image.getScaledInstance(imageComplaint.getWidth(), imageComplaint.getHeight(), Image.SCALE_SMOOTH);
                imageComplaint.setIcon(new ImageIcon(scaledImage));
                imageComplaint.setText(""); // Xóa text "Image Preview"

            } catch (Exception e) {
                JOptionPane.showMessageDialog(this, "Không thể tải hình ảnh.", "Lỗi", JOptionPane.ERROR_MESSAGE);
                e.printStackTrace();
            }
        }
    }//GEN-LAST:event_addImageActionPerformed

