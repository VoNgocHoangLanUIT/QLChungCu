    private void populateData() {
        DecimalFormat formatter = new DecimalFormat("#,##0");
        SimpleDateFormat dateFormat = new SimpleDateFormat("dd-MM-yyyy");

        idField.setText(String.valueOf(invoiceToUpdate.getInvoiceId()));
        dateField.setText(dateFormat.format(invoiceToUpdate.getCreationDate()));
        totalFeeField.setText(formatter.format(invoiceToUpdate.getTotalFee()));
        residentIdField.setText(invoiceToUpdate.getResidentId());
        cashReceivedField.setText(formatter.format(invoiceToUpdate.getCashReceived()));
        changeReturnedField.setText(formatter.format(invoiceToUpdate.getChangeReturned()));
        statusComboBox.setModel(new javax.swing.DefaultComboBoxModel<>(new String[] { "Pending", "Completed"}));
        statusComboBox.setSelectedItem(invoiceToUpdate.getStatus());
    }

    private void addListeners() {
        cashReceivedField.getDocument().addDocumentListener(new DocumentListener() {
            @Override
            public void insertUpdate(DocumentEvent e) {
                calculateChange();
            }
            @Override
            public void removeUpdate(DocumentEvent e) {
                calculateChange();
            }
            @Override
            public void changedUpdate(DocumentEvent e) {
                calculateChange();
            }
        });
    }

    private void calculateChange() {
        try {
            double totalFee = Double.parseDouble(totalFeeField.getText().replace(",", ""));
            double cashReceived = Double.parseDouble(cashReceivedField.getText().replace(",", ""));
            double change = cashReceived - totalFee;
            
            DecimalFormat formatter = new DecimalFormat("#,##0");
            changeReturnedField.setText(formatter.format(change));
            
           
            
        } catch (NumberFormatException e) {
            changeReturnedField.setText("0");
        }
    }

