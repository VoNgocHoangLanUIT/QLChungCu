    private void updateParkingSlotButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_updateParkingSlotButtonActionPerformed
        // TODO add your handling code here:
        try {
            Invoice updatedInvoice = new Invoice();
            updatedInvoice.setInvoiceId(Integer.parseInt(idField.getText()));
            updatedInvoice.setTotalFee(Double.parseDouble(totalFeeField.getText().replace(",", "")));
            updatedInvoice.setResidentId(residentIdField.getText().trim());
            
            String cashReceivedText = cashReceivedField.getText().replace(",", "");
            double cashReceived = cashReceivedText.isEmpty() ? 0 : Double.parseDouble(cashReceivedText);
            updatedInvoice.setCashReceived(cashReceived);

            String changeReturnedText = changeReturnedField.getText().replace(",", "");
            double changeReturned = changeReturnedText.isEmpty() ? 0 : Double.parseDouble(changeReturnedText);
            updatedInvoice.setChangeReturned(changeReturned);
            
            // Logic cập nhật trạng thái khi nhấn nút Update
            String status = statusComboBox.getSelectedItem().toString();
            if (!cashReceivedText.isEmpty() && cashReceived >= updatedInvoice.getTotalFee()) {
                status = "Completed";
            }
            updatedInvoice.setStatus(status);

            if (invoiceService.updateInvoice(updatedInvoice)) {
                JOptionPane.showMessageDialog(this, "Invoice updated successfully!");
                this.dispose();
            } else {
                JOptionPane.showMessageDialog(this, "Failed to update invoice.", "Error", JOptionPane.ERROR_MESSAGE);
            }
        } catch (NumberFormatException e) {
            JOptionPane.showMessageDialog(this, "Invalid number format.", "Error", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_updateParkingSlotButtonActionPerformed

