    private void addImageActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_addImageActionPerformed
        // TODO add your handling code here:
        JFileChooser fileChooser = new JFileChooser();
        FileNameExtensionFilter filter = new FileNameExtensionFilter("Image Files", "jpg", "png", "gif", "jpeg");
        fileChooser.setFileFilter(filter);
        if (fileChooser.showOpenDialog(this) == JFileChooser.APPROVE_OPTION) {
            selectedImageFile = fileChooser.getSelectedFile();
            try {
                ImageIcon imageIcon = new ImageIcon(selectedImageFile.getAbsolutePath());
                Image scaledImage = imageIcon.getImage().getScaledInstance(imageLabel.getWidth(), imageLabel.getHeight(), Image.SCALE_SMOOTH);
                imageLabel.setIcon(new ImageIcon(scaledImage));
                imageLabel.setText("");
            } catch (Exception ex) {
                JOptionPane.showMessageDialog(this, "Could not load image.", "Error", JOptionPane.ERROR_MESSAGE);
            }
        }
    }//GEN-LAST:event_addImageActionPerformed

    private void updateButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_updateButtonActionPerformed
        // TODO add your handling code here:
        try {
            Facility updatedFacility = new Facility();
            updatedFacility.setServiceId(idField.getText()); // ID is not changed
            updatedFacility.setServiceName(nameField.getText().trim());
            updatedFacility.setManufacturer(manufacturerField.getText().trim());
            updatedFacility.setUnit(unitField.getText().trim());
            updatedFacility.setStockQuantity(stockField.getText().trim());
            updatedFacility.setPrice(Double.parseDouble(priceField.getText().trim()));
            updatedFacility.setDescription(descriptionArea.getText().trim());

            if (facilityService.updateFacility(updatedFacility)) {
                if (selectedImageFile != null) {
                    saveFacilityImage(updatedFacility.getServiceId());
                }
                JOptionPane.showMessageDialog(this, "Facility updated successfully!");
                this.dispose();
            } else {
                JOptionPane.showMessageDialog(this, "Failed to update facility.", "Database Error", JOptionPane.ERROR_MESSAGE);
            }
        } catch (NumberFormatException e) {
            JOptionPane.showMessageDialog(this, "Invalid number format for Price or Stock Quantity.", "Input Error", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_updateButtonActionPerformed

