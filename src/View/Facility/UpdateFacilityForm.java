    private void populateData() {
        idField.setText(facilityToUpdate.getServiceId());
        nameField.setText(facilityToUpdate.getServiceName());
        manufacturerField.setText(facilityToUpdate.getManufacturer());
        unitField.setText(facilityToUpdate.getUnit());
        stockField.setText(facilityToUpdate.getStockQuantity());
        priceField.setText(String.valueOf(facilityToUpdate.getPrice()));
        descriptionArea.setText(facilityToUpdate.getDescription());

        // Load existing image
        String imagePath = "/icon/" + facilityToUpdate.getServiceId() + ".jpg";
        java.net.URL imgUrl = getClass().getResource(imagePath);
        if (imgUrl != null) {
            ImageIcon icon = new ImageIcon(imgUrl);
            Image scaledImage = icon.getImage().getScaledInstance(imageLabel.getWidth(), imageLabel.getHeight(), Image.SCALE_SMOOTH);
            imageLabel.setIcon(new ImageIcon(scaledImage));
            imageLabel.setText("");
        } else {
            imageLabel.setText("No Image");
        }
    }

    private void saveFacilityImage(String facilityId) {
        if (selectedImageFile == null) return;
        try {
            Path destinationPath = Paths.get("src/icon/" + facilityId + ".jpg");
            Files.createDirectories(destinationPath.getParent());
            Files.copy(selectedImageFile.toPath(), destinationPath, StandardCopyOption.REPLACE_EXISTING);
        } catch (IOException e) {
            JOptionPane.showMessageDialog(this, "Error saving image: " + e.getMessage(), "Image Save Error", JOptionPane.ERROR_MESSAGE);
            e.printStackTrace();
        }
    }

