    private void saveFacilityImage(String facilityId) {
        if (selectedImageFile == null) return;
        try {
            String originalFileName = selectedImageFile.getName();
            String extension = "";
            int i = originalFileName.lastIndexOf('.');
            if (i > 0) {
                extension = originalFileName.substring(i);
            }
            // Luôn lưu ảnh dưới dạng .jpg để dễ dàng truy vấn lại
            Path destinationPath = Paths.get("src/icon/" + facilityId + ".jpg");
            Files.createDirectories(destinationPath.getParent());
            Files.copy(selectedImageFile.toPath(), destinationPath, StandardCopyOption.REPLACE_EXISTING);
        } catch (IOException e) {
            JOptionPane.showMessageDialog(this, "Error saving image: " + e.getMessage(), "Image Save Error", JOptionPane.ERROR_MESSAGE);
            e.printStackTrace();
        }
    }

