    private void addImageActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_addImageActionPerformed
        // TODO add your handling code here:
        JFileChooser fileChooser = new JFileChooser();
        FileNameExtensionFilter filter = new FileNameExtensionFilter("Image Files", "jpg", "png", "gif", "jpeg");
        fileChooser.setFileFilter(filter);
        if (fileChooser.showOpenDialog(this) == JFileChooser.APPROVE_OPTION) {
            selectedImageFile = fileChooser.getSelectedFile();
            try {
                ImageIcon imageIcon = new ImageIcon(selectedImageFile.getAbsolutePath());
                Image scaledImage = imageIcon.getImage().getScaledInstance(imageLabel.getWidth(), imageLabel.getHeight(), Image.SCALE_SMOOTH);
                imageLabel.setIcon(new ImageIcon(scaledImage));
                imageLabel.setText("");
            } catch (Exception ex) {
                JOptionPane.showMessageDialog(this, "Could not load image.", "Error", JOptionPane.ERROR_MESSAGE);
            }
        }
    }//GEN-LAST:event_addImageActionPerformed

    private void addButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_addButtonActionPerformed
        // TODO add your handling code here:
        try {
            String facilityId = idField.getText().trim();
            if(facilityId.isEmpty()) {
                JOptionPane.showMessageDialog(this, "Facility ID cannot be empty.", "Input Error", JOptionPane.ERROR_MESSAGE);
                return;
            }

            Facility newFacility = new Facility();
            newFacility.setServiceId(facilityId);
            newFacility.setServiceName(nameField.getText().trim());
            newFacility.setManufacturer(manufacturerField.getText().trim());
            newFacility.setUnit(unitField.getText().trim());
            newFacility.setStockQuantity(stockField.getText().trim());
            newFacility.setPrice(Double.parseDouble(priceField.getText().trim()));
            newFacility.setDescription(descriptionArea.getText().trim());

            if (facilityService.addFacility(newFacility)) {
                saveFacilityImage(facilityId);
                JOptionPane.showMessageDialog(this, "Facility added successfully!");
                this.dispose();
            } else {
                JOptionPane.showMessageDialog(this, "Failed to add facility. Check console for errors.", "Database Error", JOptionPane.ERROR_MESSAGE);
            }
        } catch (NumberFormatException e) {
            JOptionPane.showMessageDialog(this, "Invalid number format for Price or Stock Quantity.", "Input Error", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_addButtonActionPerformed

