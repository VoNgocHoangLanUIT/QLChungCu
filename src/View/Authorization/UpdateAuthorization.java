    private void populateData() {
        username.setText(account.getUsername());
        email.setText(account.getEmail());
        
        String residentId = account.getResidentId();
        residentID.setText(residentId == null ? "" : residentId); // Hiển thị chuỗi rỗng nếu là null
        
        String accountRole = account.getRole();
        if (accountRole == null) {
            role.setSelectedIndex(-1); // Không chọn gì nếu chưa có vai trò
        } else {
            role.setSelectedItem(accountRole);
        }
    }

