    private void updateParkingSlotButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_updateParkingSlotButtonActionPerformed
        // TODO add your handling code here:
        String user = username.getText();
        String resId = residentID.getText().trim();
        String selectedRole = (String) role.getSelectedItem();
        
        if (selectedRole == null) {
            JOptionPane.showMessageDialog(this, "Please select a role.", "Input Error", JOptionPane.ERROR_MESSAGE);
            return;
        }

        boolean success = authzService.updateAuthorization(user, resId, selectedRole);
        
        if (success) {
            JOptionPane.showMessageDialog(this, "Authorization updated successfully!", "Success", JOptionPane.INFORMATION_MESSAGE);
            this.dispose();
        } else {
            JOptionPane.showMessageDialog(this, "Failed to update authorization.", "Error", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_updateParkingSlotButtonActionPerformed

